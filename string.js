 // O objeto global String é um construtor para strings, ou uma sequência de caracteres. Ele é usado para criar objetos de string, ou para converter outros tipos de dados em strings.

// Criando uma string usando o construtor String
const string = new String("Hello, World!");
console.log(string); // Expected output: [String: 'Hello, World!']

// DESCRIÇAO -- Strings são úteis para guardar dados que podem ser representados em forma de texto. Uma das operações mais usadas nas strings é checar seu tamanho, para construir e concatená-las usando os operadores + e +=, checando pela existência ou posição de substrings com o método indexOf , ou extrair substrings com o método substring.